package IfElse;

import java.util.Scanner;

public class ifElse {
	public static void main(String[] args) {
		
	// DECLARANDO E INICIALIZANDO A VARIÁVEL
	int numero = 29;
	
	// Se = if  - Senão = else
	if (numero > 5 ) {
		System.out.println("Sim, o numero " + numero + " é maior que 5.");	
		}
	if (numero > 9) {
		System.out.println("O numero " + numero + " é maior ou igual a 9." );
		}
	else if  (numero > 11) {
		System.out.println("Esse numero está fora do determinado.");
		}
	}
}
	
	


